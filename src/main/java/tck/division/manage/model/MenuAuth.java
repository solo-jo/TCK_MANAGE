package tck.division.manage.model;

public class MenuAuth {
	private int acct_no;
	private String menu_code;
	private String reg_id;
	private String reg_dt;
	private String upt_id;
	private String upt_dt;
	
	public int getAcct_no() {
		return acct_no;
	}
	public void setAcct_no(int acct_no) {
		this.acct_no = acct_no;
	}
	public String getMenu_code() {
		return menu_code;
	}
	public void setMenu_code(String menu_code) {
		this.menu_code = menu_code;
	}
	public String getReg_id() {
		return reg_id;
	}
	public void setReg_id(String reg_id) {
		this.reg_id = reg_id;
	}
	public String getReg_dt() {
		return reg_dt;
	}
	public void setReg_dt(String reg_dt) {
		this.reg_dt = reg_dt;
	}
	public String getUpt_id() {
		return upt_id;
	}
	public void setUpt_id(String upt_id) {
		this.upt_id = upt_id;
	}
	public String getUpt_dt() {
		return upt_dt;
	}
	public void setUpt_dt(String upt_dt) {
		this.upt_dt = upt_dt;
	}
	
	
}
